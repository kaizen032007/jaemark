# Python import Statement Cheat Sheet
#
# 1. import module
#    - Imports the whole module.
#    - Example: import math
#    - Usage: math.sqrt(16)
#
# 2. import module as alias
#    - Imports the module with a custom name (alias).
#    - Example: import numpy as np
#    - Usage: np.array([1, 2, 3])
#
# 3. from module import name
#    - Imports a specific function, class, or variable from a module.
#    - Example: from math import sqrt
#    - Usage: sqrt(16)
#
# 4. from module import name as alias
#    - Imports a specific item with a custom name.
#    - Example: from math import sqrt as square_root
#    - Usage: square_root(16)
#
# 5. from module import *
#    - Imports everything from the module (not recommended for large projects).
#    - Example: from math import *
#    - Usage: sqrt(16), sin(0), etc.
#
# 6. Importing multiple modules in one line
#    - Example: import os, sys, time
#
# 7. Importing multiple names from a module
#    - Example: from math import sin, cos, tan
#
# 8. Importing from submodules or packages
#    - Example: from datetime import datetime
#
# 9. Relative imports (in packages)
#    - Example: from . import mymodule
#    - Example: from ..subpackage import mymodule
#
# Common modules:
# import time      # Time-related functions
# import turtle    # Simple graphics (drawing)
# import math      # Math functions
# import random    # Random number generation
# import sys       # System-specific parameters and functions
# import os        # Operating system interfaces
